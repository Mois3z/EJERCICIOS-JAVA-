    private void BTNGENERARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNGENERARActionPerformed
        int num1 = (int) CTD1.getValue();
        int num2 = (int) CTD2.getValue();

        // Asegurar que num1 sea el menor
        int min = Math.min(num1, num2);
        int max = Math.max(num1, num2);

        // Generar n√∫mero aleatorio entre los dos
        Random rand = new Random();
        int numeroAleatorio = rand.nextInt(max - min + 1) + min;

        // Mostrar el resultado en el campo de texto
        FLDRESULTADO.setText(String.valueOf(numeroAleatorio)); // TODO add your handling code here:
    }//GEN-LAST:event_BTNGENERARActionPerformed

